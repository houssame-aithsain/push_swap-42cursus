#include "push_swap.h"

int	if_valid(char c)
{
	if (c == ' ' || c == '-' || c == '+')
		return (1);
	return (0);
}

int	if_digit(char c)
{
	if (c >= 48 && c <= 57)
		return (1);
	return (0);
}
